r = 30  # Given radius 30
area_of_circle = pi * r ** 2  # To calculate area of circle
print(area_of_circle)  # To print area of circle
circum_of_circle = 2 * pi * r  # To calculate circumference of circle
print(circum_of_circle)  # To print circumference of circle
